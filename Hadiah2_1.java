// Tugas Hadiah 2 Nomor 1

public class Hadiah2_1 {
	
	
	public static void main(String[] args) {
		
		int aa,bb,jumlah,bagi;
		float cc,kali;
		
		System.out.println("Ini Tugas Hadiah 2 Nomor 1");
		
		aa=5;
		bb=6;
		cc=0.5f;	
		
		System.out.println("Nilai aa : " + aa);
		System.out.println("Nilai bb : " + bb);
		System.out.println("Nilai cc : " + cc);
		System.out.println("\n");
		
		jumlah = aa + bb;
		System.out.println("Hasil dari aa + bb : " + jumlah);
		
		kali = aa*bb*cc;
		System.out.println("Hasil dari aa x bb x cc : " + kali);
		
		bagi = (int) (bb / cc);
		System.out.println("Hasil dari bb / cc : " + bagi);		
		
		
	}
	
	
	
}